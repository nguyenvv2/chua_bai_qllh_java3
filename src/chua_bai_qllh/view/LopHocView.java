    public void loadComboBox(ArrayList<String> list) {
        defaultComboBoxModel = (DefaultComboBoxModel) cbMaBoMon.getModel();
        for (String string : list) {
            defaultComboBoxModel.addElement(string);
        }
    }

    public void loadData(ArrayList<LopHoc> list) {
        defaultTableModel = (DefaultTableModel) tbLopHoc.getModel();
        defaultTableModel.setRowCount(0);
        for (LopHoc lopHoc : list) {
            defaultTableModel.addRow(new Object[]{
                lopHoc.getMaLopHoc(), lopHoc.getTenLopHoc(),
                lopHoc.getBoMon().getTenBoMon(), lopHoc.getSiSo()
            });
        }
    }

