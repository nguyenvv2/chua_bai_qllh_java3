    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        LopHoc lopHoc = new LopHoc();
        String tenLop = txtTenLop.getText();
        String maBoMon = cbMaBoMon.getSelectedItem().toString();
        int siSo = Integer.parseInt(txtSiSo.getText());

        lopHoc.setTenLopHoc(tenLop);
        lopHoc.setSiSo(siSo);
        
        BoMon boMon = new BoMon();
        boMon.setMaBoMon(maBoMon);
        
        lopHoc.setBoMon(boMon);
        
        if (lopHocService.addNew(lopHoc)) {
            JOptionPane.showMessageDialog(this, "Them thanh cong");
            loadData(lopHocService.getListLopHoc());
        }
    }//GEN-LAST:event_btnThemActionPerformed

